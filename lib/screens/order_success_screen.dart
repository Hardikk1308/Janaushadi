import 'package:flutter/material.dart';
import 'package:jan_aushadi/screens/MainApp.dart';

class OrderSuccessScreen extends StatefulWidget {
  final String orderId;
  final double totalAmount;
  final String paymentMethod;
  final Map<int, int>? cartItems;
  final Map<String, dynamic>? trackingData;
  final VoidCallback? onBackPressed;

  const OrderSuccessScreen({
    super.key,
    required this.orderId,
    required this.totalAmount,
    required this.paymentMethod,
    this.cartItems,
    this.trackingData,
    this.onBackPressed,
  });

  @override
  State<OrderSuccessScreen> createState() => _OrderSuccessScreenState();
}

class _OrderSuccessScreenState extends State<OrderSuccessScreen>
    with TickerProviderStateMixin {
  late AnimationController _checkmarkController;
  late Animation<double> _checkmarkAnimation;

  @override
  void initState() {
    super.initState();
    _checkmarkController = AnimationController(
      duration: const Duration(milliseconds: 800),
      vsync: this,
    );

    _checkmarkAnimation = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(parent: _checkmarkController, curve: Curves.elasticOut),
    );

    _checkmarkController.forward();
  }

  @override
  void dispose() {
    _checkmarkController.dispose();
    super.dispose();
  }

  String _getEstimatedDelivery() {
    final tomorrow = DateTime.now().add(const Duration(days: 1));
    final months = [
      'Jan',
      'Feb',
      'Mar',
      'Apr',
      'May',
      'Jun',
      'Jul',
      'Aug',
      'Sep',
      'Oct',
      'Nov',
      'Dec',
    ];
    return 'Tomorrow, ${tomorrow.day} ${months[tomorrow.month - 1]}';
  }

